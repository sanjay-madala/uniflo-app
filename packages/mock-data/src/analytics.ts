// Helper: generate sparkline data
// ====================================================================

function generateSparkline(
  base: number,
  variance: number,
  count: number,
  trend: "up" | "down" | "flat" = "flat"
): Array<{ name: string; value: number }> {
  const points: Array<{ name: string; value: number }> = [];
  for (let i = 0; i < count; i++) {
    const trendAdj =
      trend === "up" ? (i / count) * variance * 2 : trend === "down" ? -(i / count) * variance * 2 : 0;
    const value = Math.max(0, Math.round(base + (Math.random() - 0.5) * variance + trendAdj));
    points.push({ name: `D${i + 1}`, value });
  }
  return points;
}

