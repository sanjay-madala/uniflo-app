function generateTrendData(): TrendDataPoint[] {
  const data: TrendDataPoint[] = [];
  const startDate = new Date("2025-12-15");

  for (let i = 0; i < 90; i++) {
    const d = new Date(startDate);
    d.setDate(d.getDate() + i);
    const dateStr = d.toISOString().split("T")[0];
    const month = `${String(d.getMonth() + 1).padStart(2, "0")}/${String(d.getDate()).padStart(2, "0")}`;

    data.push({
      date: month,
      compliance: Math.round(82 + Math.random() * 12 + (i / 90) * 3),
      csat: Math.round(75 + Math.random() * 15 + (i / 90) * 2),
      ticket_volume: Math.round(5 + Math.random() * 10),
      resolution_hours: +(3 + Math.random() * 4).toFixed(1),
      sla_compliance: Math.round(88 + Math.random() * 10),
      audit_score: Math.round(78 + Math.random() * 15),
    });
  }
  return data;
}

